package main

import (
	"fmt"

	"github.com/jinzhu/gorm"
	_ "github.com/lib/pq"
)

const (
	host     = "localhost"
	port     = 5432
	user     = "lenslocked_dev"
	password = "abc123"
	dbname   = "lenslocked_dev"
)

type User struct {
	gorm.Model
	Name string
	Email string `gorm:"not null;unique_index"`
}

func main() {
	psqlInfo := fmt.Sprintf("host=%s port=%d user=%s "+
		"password=%s dbname=%s sslmode=disable",
		host, port, user, password, dbname)
	db, err := gorm.Open("postgres", psqlInfo)
	if err != nil {
		panic(err)
	}
	defer db.Close()

	db.LogMode(true)

	var u User

	db.First(&u)
	if db.Error != nil {
		panic(db.Error)
	}
	fmt.Println(u)


}
